

function openOverlayAddTask() {
    let openOverlayAddTask = document.getElementById('content-add-task-overlay');
    openOverlayAddTask.classList.add('d-none');

}

function closeOverlayAddTask() {
    let closeOverlayAddTask = document.getElementById('content-add-task-overlay');
    closeOverlayAddTask.classList.remove('d-none')

}